#config.py is a Python file commonly used in projects to store configuration settings, such as API keys, database credentials, environment variables, and application-specific constants.
# The config.py file is usually located in the app directory of a project and is imported into other modules to access configuration settings.
# The config.py file can be used to define global variables, constants, and settings that are used throughout the project.
# example: db_url = "mysql://user:password@localhost/db_name", api_key = "1234567890", log_level = "INFO"


# Instead of hardcoding settings in multiple files, config.py provides a single location for all configurations.